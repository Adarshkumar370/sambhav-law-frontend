      <p>
        Para 12: Another aspect needs to be addressed. The question we intend to
        pose is whether adultery should be treated as a criminal offence. Even
        assuming that the new definition of&nbsp;adultery encapsules within its
        scope sexual intercourse with an unmarried woman or a widow, adultery is
        basically associated with the institution of marriage. There is no
        denial of the fact that marriage is treated as a social institution and
        regard being had to various aspects that social history has witnessed in
        this country, the Parliament has always made efforts to maintain the
        rights of women. For instance,&nbsp;<strong>[Section 1]</strong>
        &nbsp;IPC deals with husband or relative of husband of a woman
        subjecting her to cruelty. The Parliament has also brought in the
        Protection of Women from&nbsp;Domestic Violence Act, 2005. This
        enactment protects women. It also enters into the matrimonial sphere.
        The offences under the provisions of the said enactment are different
        from the provision that has been conceived of under&nbsp;
        <strong>[Section 2]</strong>&nbsp;IPC or, for that matter, concerning
        bringing of adultery within the net of a criminal offence. There can be
        no shadow of doubt that adultery can be a ground for any kind of civil
        wrong including dissolution of marriage. But the pivotal question is
        whether it should be treated as a criminal offence. When we say so, it
        is not to be understood that there can be any kind of social licence
        that destroys the matrimonial home. It is an ideal condition when the
        wife and husband maintain their loyalty. We are not&nbsp;commenting on
        any kind of ideal situation but, in fact, focusing on whether the act of
        adultery should be treated as a criminal offence. In this context, we
        are reminded of what Edmund Burke, a famous thinker, had said, a good
        legislation should be fit and equitable so that it can have a right to
        command obedience. Burke would like to put it in two compartments,
        namely, equity &lsquo;and utility &lsquo;. If the principle of Burke
        is properly understood, it conveys those laws and legislations are
        necessary to serve and promote a good life.
      </p>
