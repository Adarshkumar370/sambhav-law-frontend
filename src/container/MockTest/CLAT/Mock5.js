      <p>
        Para 8: Mr Kapil Sibal, Mr Amit Desai and Mr Chander Uday Singh are
        undoubtedly right in submitting that the procedural hierarchy of courts
        in matters concerning the grant of bail needs to be respected. However,
        there was a failure of the High Court to discharge its adjudicatory
        function at two levels &ndash; first in declining to evaluate prima
        facie at the interim stage in a petition for quashing the FIR as to
        whether an arguable case has been made out, and secondly, in declining
        interim bail, as a consequence of its failure to render a prima facie
        opinion on the first.
      </p>
      <p>
